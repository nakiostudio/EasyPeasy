import Cocoa

extension NSColor {
    
    static func easy_blue() -> NSColor {
        return NSColor(red: 112.0/255.0, green: 144.0/255.0, blue: 173.0/255.0, alpha: 1.0)
    }
    
    static func easy_backgroundBlue() -> NSColor {
        return NSColor(red: 35.0/255.0, green: 71.0/255.0, blue: 113.0/255.0, alpha: 1.0)
    }
    
    static func easy_highlightedBlue() -> NSColor {
        return NSColor(red: 76.0/255.0, green: 158.0/255.0, blue: 235.0/255.0, alpha: 1.0)
    }
    
}
